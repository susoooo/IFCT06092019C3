    public String sincero(double resultado){
        String retorno="";
        retorno=Double.toString(resultado);
        if(resultado%1==0){
            // Queremos recortar 2 caracteres al string
            retorno=retorno.substring(0, retorno.length()-2);
        }
        return retorno;
    }
    
