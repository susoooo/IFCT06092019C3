    public static void copiarTexto(String cadena, String nuevo) throws FileNotFoundException, IOException {
        
        try {
                File n=new File(nuevo);
                FileWriter escribir = new FileWriter(n, true);
                escribir.write(cadena);
                escribir.close();
                
                
        } catch (IOException ioe){
                ioe.printStackTrace();
        }
     }
    
     public static void copiarArchivos(String archivo, String nuevo) throws FileNotFoundException, IOException {
                File origen = new File(archivo);
                File destino = new File(nuevo);

                try {
                        InputStream in = new FileInputStream(origen);
                        OutputStream out = new FileOutputStream(destino);

                        byte[] buf = new byte[1024];
                        int len;

                        while ((len = in.read(buf)) > 0) {
                                out.write(buf, 0, len);
                        }

                        in.close();
                        out.close();
                } catch (IOException ioe){
                        ioe.printStackTrace();
                }
     }
     
