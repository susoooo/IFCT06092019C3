    private Float Conversion(Float Cantidad, String Opcion) {
        Float Conversion;
        
        switch(Opcion) { 
            case "ADolar" -> {
                Conversion = Cantidad * 1.14f;
            }
            
            case "AEuro" -> {
                Conversion = Cantidad * 0.87f;
            }
            
            default -> {
                System.out.println("Opcion de conversion invalida");
                Conversion = null;
            }
        }
        
        return Conversion;
    }
