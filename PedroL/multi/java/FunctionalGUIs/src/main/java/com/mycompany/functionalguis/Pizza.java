    private void UpdateIngredientSummary() {
        String IngreSummary = "";
        int SelectedCount = 0;
        
        for(var Ingre : ChkIngredientes) {
            if(Ingre.isSelected()) { 
                IngreSummary += Ingre.getText() + "<br>";
                SelectedCount++;
            }
        }
        
        if(SelectedCount > 0) { 
            ChkIngre.setSelected(true);
            IngreSummary = IngreSummary.substring(0, IngreSummary.length() - 4);
            IngreSummary = String.format("<html>%s</html>", IngreSummary);
        }
        else { 
            ChkIngre.setSelected(false);
            IngreSummary = DefaultIngreSummary;
        }
        
        LblListaIngre.setText(IngreSummary);
    }
