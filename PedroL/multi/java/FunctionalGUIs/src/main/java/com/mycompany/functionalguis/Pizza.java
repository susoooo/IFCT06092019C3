    private Float CalcularMonto() {
        Float Monto = 0.0f;
        
        for(var Rdo : RdoBases) {
            if(Rdo.isSelected()) { 
                Monto += PreciosBases.get(Rdo.getText());
                break;
            }
        }
        for(var Chk : ChkIngredientes) {
            if(Chk.isSelected()) { 
                Monto += PreciosIngre.get(Chk.getText());
            }
        }
        
        return (float)Math.floor( (Monto * Descuento) * 100) / 100;
    }
    
    private String SelectedDescue() {
        String Selected = "";
        
        for(var Rdo : RdoDescue) {
            if(Rdo.isSelected()) { 
                Selected = Rdo.getText();
                break;
            }
        }
        
        return Selected;
    }
    
