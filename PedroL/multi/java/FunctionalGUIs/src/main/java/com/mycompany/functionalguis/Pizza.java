    private void BtnComprarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnComprarMouseReleased
        TicketDatosPizza = 
            "Base:\n" +
            LblListaBase.getText() + "\n\n" +
            "Ingredientes:\n" +
            LblListaIngre.getText()
                .replace("<html>", "")
                .replace("</html>", "")
                .replace("<br>", "\n")
            + "\n\n";
        TicketDatosDescue = 
            "Descuento:\n" +
            SelectedDescue() + "% - Codigo:" + FieldCodigo.getText() + "\n\n";
        TicketDatosDireccion = 
            "Direccion de domicilio:\n" +
            FieldNombre.getText() + "\n" +
            FieldDireccion1.getText() + "\n" +
            FieldDireccion2.getText() + "\n\n";
        TicketMonto =
            "Monto a pagar:\n" +
            CalcularMonto().toString();
        String TicketText = TicketDatosPizza; 
        
        if(FieldCodigo.isEnabled() && FieldCodigo.getText().isBlank()) {
            JOptionPane.showMessageDialog(null,
                    "Introduzca un código de descuento, o seleccione sin descuento",
                    "Opcion invalida",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if(FieldCodigo.isEnabled()) { TicketText += TicketDatosDescue; }
        if(RdoDomicilio.isSelected()) { TicketText += TicketDatosDireccion; }
        else { TicketText += "Pedido para recoger\n\n"; }
        TicketText += TicketMonto;
        AreaTicket.setText(TicketText);
        
        FrameTicket.setLocationRelativeTo(null);
        FrameTicket.setSize(291, 500);
        FrameTicket.setVisible(true);
    }//GEN-LAST:event_BtnComprarMouseReleased

