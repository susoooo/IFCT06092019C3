    private void BtnGuardarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnGuardarMouseReleased
        //Agrega los valores introducidos a la tabla como nueva fila
        Object[] RowData = new Object[]{
            FieldNombres.getText(),
            FieldApellidos.getText(),
            FieldDNI.getText(),
            FieldHoraE.getText(),
            FieldHoraS.getText(),
        };
        TablaModel.addRow(RowData);
        
        //Guarda la nueva fila en el archivo
        try(FileWriter OutWriter = new FileWriter(DataFile, true)) {
            
            int ColsInTabla = TablaModel.getColumnCount(), RowsInTabla = TablaModel.getRowCount();
            String RowToSave = "";
            
            for(int i = 0; i < ColsInTabla; i++) { 
                RowToSave += TablaModel.getDataVector().elementAt(RowsInTabla - 1).elementAt(i).toString() + ',';
            }
            OutWriter.write(RowToSave + '\n');
            OutWriter.close();
        } catch (IOException ex) {
            Logger.getLogger(FileCopy.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnGuardarMouseReleased

    private void BtnEliminarRegistroMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnEliminarRegistroMouseReleased
        int[] SelectedRows = Tabla.getSelectedRows();
        
        for(int Row : SelectedRows){ TablaModel.removeRow(Row); }
        
        //Rewrites the entire goddamned database file
        try(FileWriter OutWriter = new FileWriter(DataFile)) {
            int ColsInTabla = TablaModel.getColumnCount(), 
                RowsInTabla = TablaModel.getRowCount();
            String RowsToSave = "";
            
            for(int i = 0; i < RowsInTabla; i++) {
                for(int j = 0; j < ColsInTabla; j++) {
                    RowsToSave += TablaModel.getDataVector().elementAt(i).elementAt(j).toString() + ',';
                }
                RowsToSave += '\n';
            }
            OutWriter.write(RowsToSave);
            OutWriter.close();
        } catch (IOException ex) {
            Logger.getLogger(FileCopy.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtnEliminarRegistroMouseReleased

