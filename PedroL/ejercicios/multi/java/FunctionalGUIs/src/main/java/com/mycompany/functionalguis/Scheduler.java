    private void ActualizarResumen(Campos CualesDatos) {
        switch(CualesDatos) {
        case NOMBRES -> { LblDRNombres.setText("Nombres: " + FieldNombres.getText()); }
        case APELLIDOS -> { LblDRApellidos.setText("Apellidos: " + FieldApellidos.getText()); }
        case DNI -> { LblDRDNI.setText("DNI: " + FieldDNI.getText()); }
        case HORAE -> { LblDRHoraE.setText("Hora de entrada: " + FieldHoraE.getText()); }
        case HORAS -> { LblDRHoraS.setText("Hora de salida: " + FieldHoraS.getText()); }
        }
    }
    
