    public float Calcubase(){
         
         base = ((String)CBbase.getSelectedItem());
        
        if(base.equals("hiperextramegafina"))
        {
            preciobase = 0.8f;
            base = base+" "+0.8;
        }
        
        if(base.equals("hipermegafina"))
        {
            preciobase = 0.5f;
            base=base+" "+0.5;
        }
        
        if(base.equals("extrahiperfina"))
        {
            preciobase = 0.4f;
            base=base+" "+0.4;
        }
        
        if(base.equals("megaextrafina"))
        {
            preciobase = 0.7f;
            base=base+" "+0.7;
        }
        
        if(base.equals("extrafina"))
        {    
            preciobase = 0.95f;
            base=base+" "+0.95;
        }
        
        if(base.equals("megafina"))
        {
            preciobase = 0.85f;
            base=base+" "+0.85;
        }
        
        if(base.equals("hiperfina"))
        {
            preciobase = 0.82f;
            base=base+" "+0.82;
        }
        
        if(base.equals("fina"))
        {
            preciobase = 0.5f;
            base=base+" "+0.5;
        }
        
        return preciobase;
    }
    
    private void BaceptarMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BaceptarMouseReleased
        
        String ingrediente = "";
        float pingredientes;
        
        int descuento;
        
       
        preciobase=Calcubase();
        
        pingredientes=preciobase;
                
        if(TBchampiñon.isSelected()){
            ingrediente = ingrediente +"\n"+"champiñon 0.5";
            pingredientes = pingredientes + 0.5f;
        }
        
        if(TBchistorra.isSelected()){
            ingrediente = ingrediente +"n"+"chistorra 0.6";
            pingredientes = pingredientes + 0.6f;
        }
        
        if(TBchourizo.isSelected()){
            ingrediente = ingrediente +"\n"+"chourizo 0.7";
            pingredientes = pingredientes + 0.7f;
        }
        
        if(TBgrelos.isSelected()){
            ingrediente = ingrediente +"\n"+"grelos 0.5";
            pingredientes = pingredientes + 0.5f;
        }
        
        if(TBjamón.isSelected()){
            ingrediente = ingrediente +"\n"+"jamón 0.55";
            pingredientes = pingredientes + 0.55f;
        }
        
        if(TBlubrigante.isSelected()){
            ingrediente = ingrediente +"\n"+"lubrigante 0.57";
            pingredientes = pingredientes + 0.57f;
        }
        
        if(TBmaracuya.isSelected()){
            ingrediente = ingrediente +"\n"+"maracuya 0.75";
            pingredientes = pingredientes + 0.75f;
        }
        
        if(TBmorcilla.isSelected()){
            ingrediente = ingrediente +"\n"+"morcilla 0.65";
            pingredientes = pingredientes + 0.65f;
        }
        
        if(TBpiña.isSelected()){
            ingrediente = ingrediente +"\n"+"piña 0.56";
            pingredientes = pingredientes + 0.56f;
        }
        
        if(TBpolbo.isSelected()){
            ingrediente = ingrediente +"\n"+"polbo 0.54";
            pingredientes = pingredientes + 0.54f;
        }
        
        if(TBqueso.isSelected()){
            ingrediente = ingrediente +"\n"+"queso 0.15";
            pingredientes = pingredientes + 0.15f;
        }
        
        if(RBdiez.isSelected()){pingredientes=pingredientes-pingredientes*0.10f;}
        if(RBveinte.isSelected()){pingredientes=pingredientes-pingredientes*0.20f;}
        if(RBveinticinco.isSelected()){pingredientes=pingredientes-pingredientes*0.25f;}
                      
        if(RBlocal.isSelected())
            TAmostrarInfo.setText(base+" "+ingrediente+"\n"+"Total "+pingredientes);
        else
        {
            TAmostrarInfo.setText(base+" "+ingrediente+"\n"+"Total "+pingredientes+"\n"+TAdomicilio.getText());
            TAdomicilio.setText("");
        }
        
    }//GEN-LAST:event_BaceptarMouseReleased

