    private void BotonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGuardarActionPerformed
        
        if (CampoNombre.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Introduzca nombre del archivo.");
        }
        else
        {
            File archivo = new File(CampoNombre.getText());
            listaArchivos.setSelectedFile(archivo);
            listaArchivos.showOpenDialog(jPanel1);
            
            File destino = listaArchivos.getSelectedFile();
        
            CampoRuta.setText(destino.getAbsolutePath());           
            
            try 
            {
                FileWriter contenido = new FileWriter(destino, true);
                
                contenido.write(CampoTexto.getText()+".");
                contenido.close();
            } 
            catch (IOException ex) 
            {
                System.out.println("ha habido un problema"+ ex);
                Logger.getLogger(guardaTexto.class.getName()).log(Level.SEVERE, null, ex);
            }
          
        }
        
    }//GEN-LAST:event_BotonGuardarActionPerformed

