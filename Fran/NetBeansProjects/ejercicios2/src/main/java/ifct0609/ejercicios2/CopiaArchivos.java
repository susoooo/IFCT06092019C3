    private void BotonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonSeleccionarActionPerformed
        listaArchivos.showOpenDialog(jPanel1);
        
        File fichero = listaArchivos.getSelectedFile();
        
        CampoSeleccion.setText(fichero.getAbsolutePath());
        nombreFichero = fichero.getName();
    }//GEN-LAST:event_BotonSeleccionarActionPerformed

    private void BotonDestinoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonDestinoActionPerformed
        
       
        listaArchivos.showOpenDialog(jPanel1);
        
        File destino = listaArchivos.getCurrentDirectory();
        
        if (destino.isDirectory())
        {
            CampoDestino.setText(destino.getPath() + "\\" + nombreFichero);            
        }
        else
        {
            CampoDestino.setText("EL DESTINO NO ES UN DIRECTORIO.");
        }
        
    }//GEN-LAST:event_BotonDestinoActionPerformed

    private void BotonCopiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCopiarActionPerformed
        try 
        {
            Files.copy(Paths.get(CampoSeleccion.getText()), Paths.get(CampoDestino.getText()), StandardCopyOption.REPLACE_EXISTING);
        } 
        catch (IOException ex) 
        {
            Logger.getLogger(CopiaArchivos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BotonCopiarActionPerformed

