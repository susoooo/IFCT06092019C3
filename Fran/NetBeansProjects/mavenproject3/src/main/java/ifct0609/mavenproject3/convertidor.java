    private void BotonEnEurosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonEnEurosMouseReleased
        if (CampoCantidad.getText() != "")
        {
            float cantidad = Float.parseFloat(CampoCantidad.getText());
            CampoDolares.setText("");
            CampoEuros.setText(Float.toString(cantidad / cambio));        
        }
    }//GEN-LAST:event_BotonEnEurosMouseReleased

    private void BotonEnDolaresMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonEnDolaresMouseReleased
        if (CampoEuros.getText() != "")
        {
            float cantidad = Float.parseFloat(CampoCantidad.getText());
            CampoEuros.setText("");
            CampoDolares.setText(Float.toString(cantidad * cambio));        
        }
    }//GEN-LAST:event_BotonEnDolaresMouseReleased

