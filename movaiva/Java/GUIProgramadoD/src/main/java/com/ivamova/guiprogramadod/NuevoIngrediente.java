    private void btAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAceptarActionPerformed
        nuevoIngredientes = new String[ingredientes.length + 1];
        for (int contador = 0; contador < ingredientes.length; contador++) {
            nuevoIngredientes[contador] = ingredientes[contador];
        }
        nuevoIngredientes[ingredientes.length] = txfIngrediente.getText().toLowerCase();
        nuevoPrecios = new Double[precios.length + 1];
        for (int contador = 0; contador < precios.length; contador++) {
            nuevoPrecios[contador] = precios[contador];
        }
        nuevoPrecios[precios.length] = Double.parseDouble(txfPrecio.getText());
        this.setVisible(false);
    }//GEN-LAST:event_btAceptarActionPerformed

