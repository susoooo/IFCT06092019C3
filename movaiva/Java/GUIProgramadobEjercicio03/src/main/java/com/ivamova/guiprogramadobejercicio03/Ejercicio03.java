    private void miGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miGuardarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showOpenDialog(txaTexto);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            String archivo = fileChooser.getSelectedFile().getPath();
            try {
                BufferedWriter bfEscritor = new BufferedWriter(new FileWriter(archivo));
                bfEscritor.write(txaTexto.getText());
                bfEscritor.newLine();
                bfEscritor.close();
                txaTexto.setText("");
            } catch (IOException ex) {
                System.err.print(ex);
            }
        }


    }//GEN-LAST:event_miGuardarActionPerformed

    private void miCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miCargarActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showOpenDialog(txaTexto);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            String archivo = fileChooser.getSelectedFile().getPath();
            try {
                BufferedReader bfLector = new BufferedReader(new FileReader(archivo));
                String texto;
                while ((texto = bfLector.readLine()) != null) {
                    txaTexto.append(texto);
                    txaTexto.append(System.getProperty("line.separator"));
                    
                }
                bfLector.close();
            } catch (IOException ex) {
                System.err.print(ex);
            }
        }
    }//GEN-LAST:event_miCargarActionPerformed

