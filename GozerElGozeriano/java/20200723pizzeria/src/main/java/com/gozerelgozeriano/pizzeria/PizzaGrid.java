    private void btnaction(Component comp, MouseEvent e){
        JButton btn = (JButton)comp;
        if(e.getButton()==MouseEvent.BUTTON1){
            ThePizza.setbase(btn.getText());
        }
        printPizza();
   }
    
    private void btningraction(Component comp, MouseEvent e){
        JButton btn = (JButton)comp;
        switch(e.getButton()){
            case MouseEvent.BUTTON1 -> ThePizza.addingrediente(btn.getText());
            case MouseEvent.BUTTON3 -> ThePizza.removeingrediente(btn.getText());
        }
        printPizza();
   }
    
    private void btndescaction(Component comp){
        JButton btn = (JButton)comp;
        descuento = Double.parseDouble(btn.getText());
        printPizza();
   }
    
    private String getRecipeString(){
        String pizzatext;
        pizzatext = "<font face=\"arial\"size=\"24\"<center>PIZZERIA LA MAMMA</center></font><br><br>";
        pizzatext+="<strong>BASE</strong><br>";
        pizzatext+= "&nbsp;&nbsp;&nbsp;&nbsp;" + info.getbases()[ThePizza.getbase()] + "&nbsp;&nbsp;&nbsp;&nbsp;" + info.getbaseprizes(ThePizza.getbase()) + "€<br>";
        pizzatext += "<br><strong>INGREDIENTES</strong><br>";
        for(int i=0;i<ThePizza.getingredientes().length;i++){
            if(ThePizza.getingredientes()[i]>=0){
                pizzatext += "&nbsp;&nbsp;&nbsp;&nbsp;" + info.getingr()[ThePizza.getingredientes()[i]] + "&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;" + info.getingrprizes(ThePizza.getingredientes()[i]) + "€<br>";
            }
        }
        pizzatext += "<br><h2>TOTAL&nbsp;&nbsp;&nbsp;&nbsp;" + ThePizza.getprize() +"€</h2>";
        if(descuento>0.0){
            pizzatext += "<br><h2>DESCUENTO&nbsp;&nbsp;&nbsp;&nbsp;" + descuento + "%&nbsp;&nbsp;&nbsp;&nbsp;" + (ThePizza.getprize() - (ThePizza.getprize()*descuento/100.0)+"€</h2>");
        }
        if(rb_Local.isSelected()){
            pizzatext += "<br><br><strong>Servicio local</strong>";
        }else{
            pizzatext += "<br><br><strong>Servicio domicilio</strong>";
            pizzatext += "<br>" + txt_nombre.getText();
            pizzatext += "<br>" + txt_direccion.getText();
            pizzatext += "<br>" + txt_tlf.getText();
        }
        return(pizzatext);
    }
    
    private void printPizza(){
        //totalprize = ThePizza.getprize();
        txt_resumen.setText(getRecipeString());
    }
    
    private void rb_action(){
        for(Component comp : pnl_domicilio.getComponents()){
            comp.setEnabled(!this.rb_Local.isSelected());
        }
        printPizza();
    }

