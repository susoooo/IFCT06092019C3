    private void jButton31ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton31ActionPerformed
       this.segundonumero=Float.parseFloat(this.Calculos.getText());
       switch(this.calculo){
           case "+":this.Calculos.setText(sincero(this.primernumero+this.segundonumero));break;
           case "-":this.Calculos.setText(sincero(this.primernumero-this.segundonumero));break;
           case "*":this.Calculos.setText(sincero(this.primernumero*this.segundonumero));break;
           case "/":if(this.segundonumero==0){this.Calculos.setText("No se divide entre 0");}
           else{
               this.Calculos.setText(sincero(this.primernumero/this.segundonumero));}break;
       }
    }//GEN-LAST:event_jButton31ActionPerformed

